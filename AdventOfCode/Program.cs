        static void DepthAndPosition()
        {
            int forwardPosition = 0;
            int depth = 0;
            int aim = 0;

            var puzzleList = GetDepthAndPositionPuzzleList();

            foreach(var puzzle in puzzleList)
            {
                if(puzzle.IndexOf("forward") > -1)
                {
                    var regEx = new Regex(@"forward (?<forward_value>\d+)", RegexOptions.IgnoreCase);
                    var match = regEx.Match(puzzle);
                    if (match.Success)
                    {
                        var value = int.Parse(match.Groups["forward_value"].Value);
                        forwardPosition += value;
                        depth += aim * value;
                    }   
                }
                else if (puzzle.IndexOf("down") > -1)
                {
                    var regEx = new Regex(@"down (?<down_value>\d+)", RegexOptions.IgnoreCase);
                    var match = regEx.Match(puzzle);
                    if (match.Success)
                    {
                        var value = int.Parse(match.Groups["down_value"].Value);
                        //depth += value;
                        aim += value;
                    }                        
                }
                else if (puzzle.IndexOf("up") > -1)
                {
                    var regEx = new Regex(@"up (?<up_value>\d+)", RegexOptions.IgnoreCase);
                    var match = regEx.Match(puzzle);
                    if (match.Success)
                    {
                        var value = int.Parse(match.Groups["up_value"].Value);
                        //depth -= value;
                        aim -= value;
                    }                        
                }
            }

            Console.WriteLine($"Answer: {forwardPosition * depth}");
        }

